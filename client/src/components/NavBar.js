import * as React from 'react';
import PropTypes from 'prop-types';
import AppBar from '@mui/material/AppBar';
import Box from '@mui/material/Box';
import Divider from '@mui/material/Divider';
import Drawer from '@mui/material/Drawer';
import IconButton from '@mui/material/IconButton';
import List from '@mui/material/List';
import ListItem from '@mui/material/ListItem';
import ListItemButton from '@mui/material/ListItemButton';
import ListItemText from '@mui/material/ListItemText';
import MenuIcon from '@mui/icons-material/Menu';
import Toolbar from '@mui/material/Toolbar';
import Typography from '@mui/material/Typography';
import Button from '@mui/material/Button';

////////////////////////////////////////////////////////////
import Menu from '@mui/material/Menu';
import MenuItem from '@mui/material/MenuItem';
////////////////////////////////////////////////////////////

const drawerWidth = 240;
const navItems = ['Home', 'Katalogue', 'Search', 'Forum'];

function NavBarAppBar(props) {

  ////////////////////////////////////////////////////////////
  /////////   Code for the Dropdown menu in navbar   /////////
  ////////////////////////////////////////////////////////////
  const [anchorEl, setAnchorEl] = React.useState(null);
  const open = Boolean(anchorEl);
  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };
  ////////////////////////////////////////////////////////////
  ////////////////////////////////////////////////////////////

  const { window } = props;
  const [mobileOpen, setMobileOpen] = React.useState(false);

  const handleDrawerToggle = () => {
    setMobileOpen(!mobileOpen);
  };

  const drawer = (
    <Box onClick={handleDrawerToggle} sx={{ textAlign: 'center' }}>
      <MenuIcon />
      <Typography variant="h6" sx={{ my: 2 }}>
        BingeWatcher
      </Typography>
      <Divider />
      <List>
        {navItems.map((item) => (
          <ListItem key={item} disablePadding>
            <ListItemButton sx={{ textAlign: 'center' }}>
              <ListItemText primary={item} />
            </ListItemButton>
          </ListItem>
        ))}
      </List>
    </Box>
  );

  const container = window !== undefined ? () => window().document.body : undefined;

  return (
    <Box sx={{ display: 'flex' }}>
      <AppBar component="nav">
        <Toolbar>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            edge="start"
            onClick={handleDrawerToggle}
            sx={{ mr: 2, display: { sm: 'none' } }}
          >
            <MenuIcon />
          </IconButton>
          
          <Button variant="text" component="div"
              sx={{ flexGrow: 1, display: { xs: 'none', sm: 'block' }, color:"#fff"}}>
            <Typography
              variant="h5"
            >
              BingeWatcher
            </Typography>
          </Button>


          <Box sx={{ display: { xs: 'none', sm: 'block' }}}>
            {navItems.map((item) => {
              if (item=='Katalogue'){
                return ( <> 
                <Button id="basic-button"
                  aria-controls={open ? 'basic-menu' : undefined}
                  aria-haspopup="true"
                  aria-expanded={open ? 'true' : undefined}
                  sx={{ color: '#fff'}}
                  onClick={handleClick}
                >
                Katalogue
                </Button>
                <Menu
                  id="basic-menu"
                  anchorEl={anchorEl}
                  open={open}
                  onClose={handleClose}
                  MenuListProps={{
                    'aria-labelledby': 'basic-button',
                  }}
                >
                  <MenuItem onClick={handleClose}>Movie Katalogue</MenuItem>
                  <MenuItem onClick={handleClose}>TV Katalogue</MenuItem>
                </Menu>
                
                </>
                )}
              else 
               return (
                <Button key={item} sx={{ color: '#fff' }}>
                  {item}
                </Button>
               )
            })}

          </Box>
        </Toolbar>
      </AppBar>
      <Box component="nav">
        <Drawer
          container={container}
          variant="temporary"
          open={mobileOpen}
          onClose={handleDrawerToggle}
          ModalProps={{
            keepMounted: true, // Better open performance on mobile.
          }}
          sx={{
            display: { xs: 'block', sm: 'none' },
            '& .MuiDrawer-paper': { boxSizing: 'border-box', width: drawerWidth },
          }}
        >
          {drawer}
        </Drawer>
      </Box>
    </Box>
  );
}

NavBarAppBar.propTypes = {
  /**
   * Injected by the documentation to work in an iframe.
   * You won't need it on your project.
   */
  window: PropTypes.func,
};

export default NavBarAppBar;
